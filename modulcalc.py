import math
def add(num1,num2):
    res = num1 + num2
    return res

def sub(num1,num2):
    res = num1 - num2
    return res


def mult(num1,num2):
    res = num1*num2
    return res

def div(num1,num2):
    res = num1/num2
    return res

def log(num1,num2):
    res = math.log(num1,num2)
    return res

def sqrt(num1,num2):
    res1 = math.sqrt(num1)
    res2 = math.sqrt(num2)
    return res1 , res2

